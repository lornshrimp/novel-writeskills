# 1.1.1 临时派修单背后的井口疑点

一张前置报障为空的临时派修单，一道不属于标准维保的井盖内侧新痕，一件从工具箱里消失的短柄撬片，再加上一把被擦过的荧光美工刀——这几项线索同时出现在彩虹巷的暴雨夜里，让陆深在现场第一时间排除了“街头斗殴”这一省事结论。

但在他真正说出那句“不是斗殴”之前，这条老巷已经快被另一种口径盖过去了。

深港市老城区的雨下得很猛。彩虹巷尽头，半掀的铸铁井盖斜立在墙边，井沿外躺着一具男性尸体，右手外侧放着一把荧光美工刀。蓝红警灯打进积水里，水面一晃一晃，把那把刀照得格外醒目。

先到场的辖区民警正在做初勘登记。他见陆深到了，先把基本情况压缩给他：死者男性，三十多岁，身上带着基站维保证件；巷口摊贩听见过异常动静，以为有人打架；分局催着先给一个初步口径，最好按街头斗殴先走，避免雨继续冲刷后现场无法固定。

这段话的信息量很大，但最关键的只有一句——先给口径。

对调查来说，最危险的未必是没有线索，而是某种看似顺手的判断太早被写上去。只要“街头斗殴”先进入接警摘要，后面所有不吻合的材料都要花更大成本才能把它顶回来。

陆深没有先争辩。他蹲下来，先看刀具位置，再看尸体姿态，最后看井口与井盖的关系。

第一处疑点，是刀。

那把荧光美工刀离死者右手不算很远，但方向过于工整。刀尖朝外，刀柄向内，像是为了让后来的人一眼就把它认成“凶器”。这在粗水泥地、旧油污和雨水交错的现场并不合理。真实搏斗中脱手的刀具，往往伴随翻滚、擦蹭和失控轨迹，不会安静成这样。

第二处疑点，是刀柄。

荧光来自刀柄表面的亮漆，不是刀体本色。陆深用侧光照过去，看见握持区有一圈明显拖痕，擦拭特征很强，甚至边缘比其他部位更干净。对一件可能的作案工具来说，这类痕迹意味着一个简单结论：有人在意上面留下些什么。

“先记。”陆深对辅警说，“二号物刀柄疑似人工擦拭，送检指纹残留和表面纤维。”

第三处疑点，是死者随身工具箱。

工具箱就在井口另一侧，卡扣打开，箱内大部分工具仍按分格摆放：螺丝刀、测电笔、胶布、扎带、扳手都在，唯独胆位中间一格空了出来。那一格形状适合放一件短而硬的撬类工具，海绵边缘有明显新切口。箱盖内侧资产编码为 WX-04-19，塑封工具清单编号 TL-07B。清单上写着八样常规井下作业工具，其中“短柄撬片”对应的格位正好空缺。

如果只少一件工具，可以解释成遗漏。

但问题是，井盖内侧刚好存在与它高度对应的异常痕迹。

陆深把目光转向井盖。外缘两道旧痕正常，标准起井钩使用后的痕迹很清楚；真正不对的是内侧锁耳附近那一段新伤。那道金属咬痕更窄，受力角度更陡，边缘微微翻起，明显不是常规开井操作留下的。它像被一种更尖、更硬的工具，从内侧狠狠干过一次。

“第三种工具痕。”陆深拍完照后给出判断。

这句话让一旁的辖区民警停了下来。

因为到这一步，井盖新痕和工具箱空位已经开始形成对照：有人动过这口井，而且很可能带走了最关键的那件工具。

死者身份很快也被进一步拉实。塑封证件袋内显示，死者名叫何川，是维保工程师，工牌与门禁卡都在，门禁卡编号 A区外维-07。正常情况下，这足以支撑“他是来处理维保任务”的方向。可现场另一项信息，很快又把这个方向拧弯了。

林岚到场后，从死者上衣内袋中夹出一张热敏纸回执。

临时派修单。
工单号：WX-LS-2147。
地点：西区彩虹巷禁入段边井。
签到时刻：21:32。
处理级别：加急。
最关键的是，“前置报障”一栏，空白。

一张前置报障为空的加急派修单，本身就足够引发追问。何川显然不是路过，他是被派到现场来的。但如果今晚系统里没有正式报障，这张单又是怎么出来的？如果任务本身存在问题，为什么他还是带着整箱工具赶来，最后却偏偏少了一件最关键的撬类工具？

陆深在检查工具箱空槽边框时，指尖还闪过一个极清晰的时间：21:47。那不是可以直接写进案卷的证据，却像一枚钉子，先把另一个时间——派修单上的21:32——钉在了他脑子里。两个时间相差十五分钟。

十五分钟意味着什么？

对现场重建来说，十五分钟已经足够。足够有人提前开井，足够有人布置井边物品，足够有人拿走工具，也足够把何川放到一个看起来像“冲突后倒地”的位置上。

物业的回话随后补上了这条链的最后一块信息：值班室确认，今晚这口井没有登记开盖，系统里没有对应报修。

到这里，所谓“街头斗殴”的说法已经不再只是“看上去不太像”。

而是被多条具体材料直接顶住了：

第一，刀具摆放过于工整，且刀柄存在人工擦拭痕。
第二，井盖内侧出现非标准开盖工具留下的新痕。
第三，工具箱里缺失与该痕迹高度相关的短柄撬片。
第四，临时派修单前置报障为空，却仍以加急方式把死者送到现场。
第五，物管系统反馈今晚并无正式开盖任务。

这些信息已经足够让调查方向从“随机冲突”转向“有准备的摆放现场”。

真正把这一判断钉死的，是抬尸之后露出来的地面痕迹。

尸体被抬起时，背下积水裂开，下面露出一小块更干的水泥地。地面上有一道短促拖拽弧线，方向朝向井口。痕迹不长，但足够说明死者不是自然倒在这一点上的，而是在放置过程中被微调过位置。

换句话说，尸体不是单纯地“倒在井边”。

它是被摆在井边。

陆深这才明确对辖区民警说，不要先按随机行凶定性，先按疑似案件留证。民警沉默之后，把原本写了一半的口径擦掉了。

暴雨夜里，所有系统都想尽快恢复正常：井盖复位，道路通行，电话挂断，表格归档。

但这一次，最值得注意的信息恰恰来自那些“恢复正常”之前被留下的痕迹：那把太整齐的刀，工具箱里空着的一格，派修单上空白的前置报障，21:32 与 21:47 的错位，以及井盖内侧那道第三种工具痕。

陆深最后看了一眼工具箱空位，对身边人只说了两句：

“工具箱单独封存。”
“井盖今晚不复位。”

因为真正能打开这口井的那件工具，此刻已经不在现场了。

而拿走它的人，显然也不想让任何人太快知道，何川到底为什么会出现在这里。

