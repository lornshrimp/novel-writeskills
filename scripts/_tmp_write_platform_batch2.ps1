# 1.1.1 暴雨夜，井边那把刀太整齐

如果不是那把刀太整齐，这场雨差一点就替人把事收完了。

陆深钻进彩虹巷时，警戒带已经被雨打得贴在水泥墙上。巷子很窄，井口半掀，一块沉重的铸铁井盖立在墙边，像有人故意把它竖起来，怕后来的人看不见。井沿外躺着一具男尸，右手边是一把荧光美工刀，刀柄上那几道绿得发冷的亮漆，在警灯下像刚从黑水里浮出来。

先到场的辖区民警正忙着登记，一见他就开口：“SCIU？男的，三十多，身上有维保证。巷口有人听见动静，以为打架。分局催口径，想先按街头斗殴走。再拖一会儿，雨把现场全洗干净了。”

这句话一出口，陆深心里就先沉了半寸。

他太清楚这种“先按什么走”意味着什么。案子一旦被一句最省事的话盖住，后面真要往回翻，翻的就不只是证据，还有已经写进系统里的判断。很多时候，不是现场先被破坏，是口径先把现场压扁了。

陆深没急着回话，先蹲下去看那把刀。

刀离死者右手不算很远，却远得不对。更要命的是方向。刀尖朝外，刀柄朝内，在粗水泥地上安安静静地躺着，像有人提前替它找好了“该怎么被看见”的角度。要真是雨夜狠狠干起来之后脱手，这里至少会乱一点，狼狈一点，不会像现在这样，整齐得像摆拍。

“身份呢？”他问。

辅警把塑封袋递过来：“何川，维保工程师。工牌、门禁卡都在，编号 A区外维-07。旁边这工具箱也像是他的。”

工具箱摊开着。螺丝刀、扳手、测电笔、胶布都在，只有中间一格空着。那格的形状很像撬类工具待的地方，海绵边缘还有新切出来的毛茬，像被谁急急修过。

陆深又把手电压回刀柄。

荧光涂层很冷，握持那一圈却更冷。那里有一圈细细的拖痕，像被人仔细擦过。不是用过，而是收拾过。

“动过没？”他问。

“没动。照片拍了，定位还在。”民警说完，又补了一句，“可你总不能就凭它摆得顺一点，把这里按谋杀现场封一夜吧？”

陆深把记录板接过去，在“初步判断”后面写了四个字：物证存疑。

“先把证据放前面。”他说，“别让结论先跑了。”

紧接着他点人固定三处：井口外沿、尸体背下地面、工具箱空槽。又把巷口那辆等着复位的应急车先压住。那辆车只要一动，很多东西就真没了。

雨打在井盖上，声音像有人隔着铁皮连续敲指节。陆深蹲低去看死者的手。右手虎口和指节有老茧，像长期握工具的人；指尖却太干净，不像刚狠狠干过一场。左前臂靠袖口那片擦伤沾着黑灰，感觉更像蹭过井沿、管壁或金属内侧。

“有人听见他喊吗？”他问。

“没听清，就一声闷响，像井盖砸地。”民警答，“十点零三报警，十点零八到场。”

陆深这才看井盖。

老城区常见的铸铁圆盖，外缘两道旧痕很正常，标准起井钩留下的。但内侧靠锁耳的地方，多了一道新鲜金属咬痕。那道痕更窄，角度更陡，边缘还轻轻翻起，明显不是常规开井工具干出来的。

他让人递比例尺，贴着拍完，低声说了一句：“第三种工具痕。”

民警愣住：“什么意思？”

“意思是这口井，今晚至少被一种非标准工具动过。”陆深回头看了一眼工具箱空位，“而且现场正好少了一件最该在这儿的东西。”

他把工具箱掀到最大。箱盖内侧贴着资产编码 WX-04-19，塑封清单编号 TL-07B。八样常规工具里，短柄撬片那一项，对应的格子正空着。空位边缘还粘着一点极细的灰白粉末，像金属狠狠干之后留下来的屑。

陆深隔着手套，在空槽边框上轻轻碰了一下。

那一下很短，短得像错觉。

可他还是看见了一点碎片：有人从井盖内侧发力，工具扣进去，往上一挑。紧接着，一个数字从脑子里一下闪出来——21:47。

下一秒，感觉就断了。

他把手收回来，像什么都没发生。这个时间暂时不能写进任何记录里，但它已经先扎在了脑子里，等着和别的证据碰面。

林岚就是这时候进来的。

她一身雨水，动作很快，第一句就问：“谁动过？”

“没人乱碰。”陆深说，“刀柄被擦过，井盖内侧有第三种工具痕，工具箱缺一件。”

林岚点头，转身对周围人说：“先封，再说判断。谁碰过哪儿，都写下来。”

这句话一落，现场所有人的动作都慢了下来。

有时候，程序不是拖延，是最后一道护栏。

林岚很快从死者上衣内袋里夹出一张热敏纸回执。纸边已经被雨泡软，但字还没糊。

临时派修单。
工单号：WX-LS-2147。
地点：西区彩虹巷禁入段边井。
签到时刻：21:32。
处理级别：加急。
最下面，“前置报障”一栏，空着。

这下案子的味道一下变了。

何川不是恰好路过。

他是被派来的。

可如果是正常派修，为什么前置报障会空白？如果今晚本来没有登记开盖，这张加急单又是怎么跑出来的？更关键的是，纸上写着21:32，而陆深刚才碰到空槽时闪出来的时间是21:47。十五分钟，不长不短，刚好够一个人先开井、取走工具、摆好位置，再把那把刀留在尸体旁边。

巷外的物业保安很快又回了一句：“值班室确认了，今晚这口井没排过开盖。”

到这里，所谓“街头斗殴”，已经越来越像一句为了方便才想塞进现场的解释。

“先抬尸。”林岚说。

尸体一抬，背下积水裂开，下面露出一小块更干的地面。那上头有一道短短的拖拽弧线，方向正对井口。痕不长，却够说明问题：尸体不是自然倒在这儿的，它被人调整过。

陆深看着那条弧线，终于把心里那句判断说了出来：“不是街头斗殴。”

民警一时没接话。

陆深就把证据一条一条给他摆：刀的位置不对，刀柄被擦过；井盖内侧有第三种工具痕；工具箱里少了短柄撬片；派修单前置报障空白，却是加急；尸体背下还有拖拽痕。任何一条单拿出来都能争，可几条同时落在一个现场里，就不是争，是硬顶着不肯看。

民警沉默了几秒，最后还是把原来写了一半的口径擦掉了。

雨还在下。巷口的复位车还在等。城市比谁都擅长把夜里的异常尽快恢复成“什么也没发生过”的样子。可陆深知道，这回不能顺着它走。

因为这场雨里最不对的，不只是尸体和那把刀。

而是有人太急着让这件事看起来像随机冲突。

陆深最后又看了一眼工具箱那格空位，低声对身边人说：“工具箱单独封存，井盖今晚不复位。”

停了停，他又补了一句：

“少的那件工具，还在外面。”

