# 1.1.1 井盖内侧的第三道痕

陆深抵达彩虹巷时，雨势正盛。

老城区的夜色被雨线割得支离。巷口便利店只亮着一半招牌，警灯映在积水里，被来回踩裂成不稳定的蓝红光斑。巷子最深处，一块铸铁井盖半掀着，斜靠在墙边。井沿外侧躺着一具男性尸体，肩背压在湿冷的水泥地上。尸体右手外，搁着一把荧光美工刀。

陆深在警戒带前停了两秒，先看井口，再看尸体，最后把目光落在那把刀上。

它的位置过于整齐。

这并不是一句模糊的感觉，而是一种具体的不协调：刀尖朝外，刀柄向内，距离死者右手不远，却不符合激烈冲突中武器脱手后的自然落点。地面粗糙，夹杂油污与细砂，若为搏斗脱手，至少应有翻滚或擦蹭痕迹，但刀身周围过于安静。

先到场的辖区民警正在记录现场信息，见他进来，简短说明情况：死者男性，三十余岁，身上有基站维保证件；巷口摊贩听见过异常声响，怀疑有人雨夜斗殴；分局正在催要初步口径，担心雨势继续扩大后，现场细痕难以保留。

这类话，陆深并不陌生。

许多案件最初的偏差，并不来自证据本身，而来自一条过早写下的判断。只要“街头斗殴”先被写进接警摘要，后续每一项与之不符的材料，都需要额外的时间与程序成本去推翻它。

陆深接过记录板，在“初步判断”后写下“物证存疑”四字，并签了名字。

这不是拖延，而是为现场争取必要的审慎。

他蹲至尸体右侧，先查看死者与井口的相对位置。井沿距死者右肩约四十厘米，井盖立在左后方，替地面挡去部分雨水。死者姿态并非完全自然。那把刀则更不自然。掉落物会乱，顺手扔下的工具会斜，只有被人放好的东西，才会在雨夜里还保留这种近乎克制的方向感。

“死者身份确认了吗？”陆深问。

辅警递来塑封证件袋。死者名叫何川，职业为维保工程师，工牌与门禁卡均在，门禁卡编号 A区外维-07。井口另一侧还有一只开启的金属工具箱，应为死者随身携带物。

工具箱内分格大致完整：螺丝刀、测电笔、绝缘胶布、扎带、扳手等常用工具尚在原位，唯独胆位中间一格空置。该空位形制适合放置一件短而结实的撬类工具，海绵切口较新，边缘留有二次修整过的痕迹。

陆深戴上手套，先检查美工刀。刀柄上的荧光漆在手电侧光下显得异样冷亮，握持区有明显细密拖痕，呈环状分布，边缘甚至比其他部位更干净。这是擦拭后的典型表现，而非普通使用磨损。

“起过物没有？”他问。

民警回答，尚未起物，现场照片已拍完，定位仍在。

“刀柄疑似人工擦拭。”陆深让辅警立即补记，“优先送检指纹残留与表面纤维。”

随后，他检查死者双手。右手虎口与指节存在长期用力形成的老茧，与职业特征相符；但指尖过于干净，不符合剧烈抓扯后的状态。左前臂靠近袖口处有一片擦伤，边缘沾有黑灰，更像与井沿或金属表面摩擦形成的损伤，而非刀伤。

他将手电移向井盖内侧。

老式铸铁井盖的外缘可见两处常规撬动痕，方向与宽度都正常。异常之处在里面。靠近锁耳的位置，有一道窄而新的金属咬痕，角度更陡，边缘微翻，像有人从内侧用另一种更尖、更硬的工具狠狠干进去过一次。那不是常规井钩，也不像普通扳手的辅助痕。

“到场时就开着？”他抬头问。

民警说是。报警人不敢触碰，他们也只是确认过井下无人，没有继续挪动井盖。

陆深拍完照片，低声说：“第三种工具痕。”

那句话让民警终于安静了一瞬。他顺着陆深的视线看向工具箱，像在后知后觉地意识到，井盖和那格空位也许不是两件彼此无关的事。

工具箱盖内贴着防水标签：资产编码 WX-04-19。下方塑封清单编号 TL-07B，列出八样常规工具。其中“短柄撬片”一项，对应的格位正为空缺。

空位边缘附着极细的灰白粉屑，疑似金属磨削残留。

“安全绳扣在吗？”陆深问。

辅警检查后回答：死者身上没有，工具箱内也没有。

他没有继续碰触箱内物品，只是隔着手套，用手电逐一扫过槽位与边框。指尖轻触空槽边缘的瞬间，一阵极轻的异感掠过脑海：一个从内侧发力的角度，一瞬反光，还有一个极清晰的数字——21:47。

陆深立刻收回手，没有让任何人察觉这一瞬的停顿。

这种感知无法进入程序，也无法成为证词。它所能做的，仅仅是提醒他，纸面之外，还有一段尚未被找到的时间差。

林岚随后赶到现场。

她到场后的处理一贯直接：先确认现场边界，再讨论判断。她简短听完陆深的说明，立即要求所有接触点逐一落纸备案，并明确指示在未完成封存前，任何人不得再碰那把刀。

她检查了死者外表，很快确认：正面未见明确致命刀伤，后脑部位可能存在一次钝击，具体仍需带回进一步检验。这一判断使那把摆在尸体旁边的美工刀，进一步偏向“被放置的解释物”，而非实际致命工具。

巷外很快又传来消息。物业保安隔着警戒带说，值班室刚回话，今晚这口井没有登记开盖，报修系统内也没有相应项目。

与此同时，林岚从死者内袋夹出来的热敏纸，则把另一层不对劲摊开在雨里。

临时派修单。
工单号 WX-LS-2147。
地点：西区彩虹巷禁入段边井。
签到时刻：21:32。
处理级别：加急。
而“前置报障”一栏，空白。

这张纸使死者的身份由“偶然卷入冲突的人”转变为“被派遣到现场的人”。问题随之浮出：一张前置报障为空的加急工单，是如何成立的？若今晚无登记开盖，何川为何会来到此处？若他确为正常派修人员，为何工具箱里偏偏少了与井盖异常痕迹最可能对应的那件工具？

陆深脑中同时浮现另一组时间：21:32，与刚才异感中闪过的21:47。

两者相差十五分钟。

这十五分钟足够让某个人提前打开井盖，足够取走一件工具，也足够在雨势尚未完全覆盖现场之前，对尸体与物品位置做出一次必要的调整。

“先抬尸。”林岚说，“再晚一点，地面痕迹就保不住了。”

尸体抬起时，背下积水被撕开，露出一小块略干的地面。其上有一道短促的拖拽弧线，方向朝向井口。该痕迹长度不大，几乎被鞋印破坏，却仍足以说明：死者的最终位置并非自然倒地形成，而是存在过被轻微调整的过程。

至此，“街头斗殴”的解释已难以成立。

现场至少有以下几项材料同时反驳这一判断：
其一，刀具位置异常，且刀柄存在人工擦拭痕；
其二，井盖内侧出现第三种工具痕；
其三，工具箱中缺失与该痕迹高度相关的短柄撬片；
其四，临时派修单前置报障为空，却仍以加急方式将死者送至现场；
其五，尸体背下存在指向井口的拖拽痕，显示位置曾被调整。

这些细节并不喧哗，却彼此呼应，足以说明这不是一次随机性的街头冲突，而是一处被刻意组织过的现场。

陆深对辖区民警说：“按疑似案件留证，不要先定随机行凶。”

后者看了看那把刀，又看了看派修单与井盖，终于将先前写了一半的口径擦去。

雨水仍沿井沿往下流，巷口的复位车还在等待。城市总倾向于尽快恢复表面的秩序，仿佛只要井盖重新盖回去，道路重新通行，这个夜晚就能回到正常。

但陆深知道，真正缺失的并不是“正常”。

是那件能开这口井的工具。

而它，此刻还在某个人手里。

