$ErrorActionPreference = 'Stop'

function Write-Utf8File {
    param([string]$Path,[string]$Content)
    $encoding = New-Object System.Text.UTF8Encoding($false)
    [System.IO.File]::WriteAllText($Path, $Content, $encoding)
}

function Expand-Chapter {
    param(
        [string]$Path,
        [string]$BodyAdd,
        [string]$AfterAdd
    )
    $marker = '## 作者有话说'
    $text = [System.IO.File]::ReadAllText($Path)
    $idx = $text.IndexOf($marker, [System.StringComparison]::Ordinal)
    if ($idx -lt 0) { throw "Marker not found: $Path" }
    $body = $text.Substring(0, $idx).TrimEnd()
    $after = $text.Substring($idx + $marker.Length).Trim()
    $newText = $body + "`r`n`r`n" + $BodyAdd.Trim() + "`r`n`r`n" + $marker + "`r`n`r`n" + $after + "`r`n" + $AfterAdd.Trim() + "`r`n"
    Write-Utf8File -Path $Path -Content $newText
}

