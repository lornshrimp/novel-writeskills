# 1.1.1 暴雨巷口的错误口径

彩虹巷的雨下得很旧。

不是细，也不是急，而是一种把整条巷子都泡软的旧雨。楼体墙皮被冲出深浅不一的灰，巷口便利店残着半截白灯，水从裂开的地缝里慢慢往外泛，像这条街本来就藏着一层不肯散的潮气。

陆深到的时候，井盖先被看见。

那是一块半掀的铸铁圆盖，斜立在墙边，不像维修时临时搁置，倒像谁特意把它摆在那儿，好让后来的人一眼看清这口井已经开过。井沿外有一具男性尸体，半边肩背压在积水与水泥地之间。尸体右侧，躺着一把荧光美工刀。

它太整齐了。

雨会把很多东西冲乱，也会把很多东西冲得更明白。陆深站在警戒带外看了几秒，先想到的不是凶器，而是“摆放”二字。那把刀离死者右手并不算远，却远到刚好不像是激烈扭打后的脱手；刀尖朝外，刀柄朝内，像一句已经替别人写好的答案。

辖区民警正在登记，见他进来，先把基础情况说了：死者男性，三十出头，身上带着基站维保证件；巷口摊贩听见过异常声响，怀疑有人雨夜斗殴；分局催要尽快给口径，最好先按街头斗殴走，免得现场被雨毁掉以后什么都说不清。

这是陆深最不喜欢的一种开场。

一个结论比证据早到。

老城区的夜里，总有人急着让事情快点变得“可以解释”。而一件事一旦有了最省力的解释，后来那些缓慢、麻烦、需要时间才能出现的细节，往往反而像多余。

陆深接过记录板，在“初步判断”后面写下“物证存疑”四字，并签了名字。

这不是拖延，而是为现场争取必要的审慎。

他蹲至尸体右侧，先查看死者与井口的相对位置。井沿距死者右肩约四十厘米，井盖立在左后方，替地面挡去部分雨水。死者姿态并非完全自然。那把刀则更不自然。掉落物会乱，顺手扔下的工具会斜，只有被人放好的东西，才会在雨夜里还保留这种近乎克制的方向感。

“死者身份确认了吗？”陆深问。

辅警递来塑封证件袋。死者名叫何川，职业为维保工程师，工牌与门禁卡均在，门禁卡编号 A区外维-07。井口另一侧还有一只开启的金属工具箱，应为死者随身携带物。

工具箱内分格大致完整：螺丝刀、测电笔、绝缘胶布、扎带、扳手等常用工具尚在原位，唯独胆位中间一格空置。该空位形制适合放置一件短而结实的撬类工具，海绵切口较新，边缘留有二次修整过的痕迹。

陆深戴上手套，先检查美工刀。刀柄上的荧光漆在手电侧光下显得异样冷亮，握持区有明显细密拖痕，呈环状分布，边缘甚至比其他部位更干净。这是擦拭后的典型表现，而非普通使用磨损。

“起过物没有？”他问。

民警回答，尚未起物，现场照片已拍完，定位仍在，但话里仍旧透着想快点结束的焦虑：“总不能就因为摆得太顺，就按谋杀现场封一夜吧？”

陆深没解释。他只是把那四个字往前推了推：物证存疑。

雨继续下。井里传出空洞的回响，像很远的地方有人在敲管壁。法医未到前，陆深先检查死者双手。虎口与指节有明显老茧，符合长期握工具的工作习惯；指尖却过分干净，不符合剧烈抓扯后的状态。左前臂靠近袖口处有一道擦伤，边缘沾有黑灰，更像与井沿或金属表面摩擦形成的损伤，而非刀伤。

他将手电移向井盖内侧。

老式铸铁井盖的外缘可见两处常规撬动痕，方向与宽度都正常。异常之处在里面。靠近锁耳的位置，有一道窄而新的金属咬痕，角度更陡，边缘微翻，像有人从内侧用另一种更尖、更硬的工具狠狠干进去过一次。那不是常规井钩，也不像普通扳手的辅助痕。

“到场时就开着？”他抬头问。

民警说是。报警人不敢触碰，他们也只是确认过井下无人，没有继续挪动井盖。

陆深拍完照片，低声说：“第三种工具痕。”

那句话让民警终于安静了一瞬。他顺着陆深的视线看向工具箱，像在后知后觉地意识到，井盖和那格空位也许不是两件彼此无关的事。

工具箱盖内贴着防水标签：资产编码 WX-04-19。下方塑封清单编号 TL-07B，列出八样常规工具。其中“短柄撬片”一项，对应的格位正为空缺。

空位边缘附着极细的灰白粉屑，疑似金属磨削残留。

陆深没有继续碰触箱内物品，只是隔着手套，用手电逐一扫过槽位与边框。指尖轻触空槽边缘的瞬间，一阵极轻的异感掠过脑海：一个从内侧发力的角度，一瞬反光，还有一个极清晰的数字——21:47。

陆深立刻收回手，没有让任何人察觉这一瞬的停顿。

这种感知无法进入程序，也无法成为证词。它所能做的，仅仅是提醒他，纸面之外，还有一段尚未被找到的时间差。

林岚随后赶到现场。

她到场后的处理一贯直接：先确认现场边界，再讨论判断。她简短听完陆深的说明，立即要求所有接触点逐一落纸备案，并明确指示在未完成封存前，任何人不得再碰那把刀。

她检查了死者外表，很快确认：正面未见明确致命刀伤，后脑部位可能存在一次钝击，具体仍需带回进一步检验。这一判断使那把摆在尸体旁边的美工刀，进一步偏向“被放置的解释物”，而非实际致命工具。

巷外很快又传来消息。物业保安隔着警戒带说，值班室刚回话，今晚这口井没有登记开盖，报修系统内也没有相应项目。

与此同时，林岚从死者内袋夹出来的热敏纸，则把另一层不对劲摊开在雨里。

临时派修单。
工单号 WX-LS-2147。
地点：西区彩虹巷禁入段边井。
签到时刻：21:32。
处理级别：加急。
“前置报障”一栏，空白。

这张纸让何川的身份发生了变化。他不再像一个恰好路过后卷进冲突的人，而像一个被叫到这里来的人。问题在于，谁叫他来？一张前置报障为空的加急单，是怎么走完这段路的？

陆深脑子里的另一个时间在这时浮上来。21:47。和纸面上的21:32，刚好错开十五分钟。

这十五分钟像一道安静的缝。

够一个人先开井，够一个人拿走工具，够一个人把井边与尸体之间的关系调整到“像一场冲突”而又不过分明显的地步。

“先抬尸。”林岚说，再晚一点，地面细痕就真的没了。

尸体抬起时，背下积水裂开，露出一小块略干的地面。其上有一道短促的拖拽弧线，方向朝向井口。该痕迹长度不大，几乎被鞋印破坏，却仍足以说明：死者的最终位置并非自然倒地形成，而是存在过被轻微调整的过程。

街头斗殴的说法，到这里已经不需要反驳了。

真正令人不适的，是现场突然变得过于完整。刀被擦过；井盖内侧有第三种工具痕；工具箱少了一件最关键的东西；前置报障为空的派修单却加急把人送来；尸体背下还有被调整过位置的痕迹。这些细节都不喧哗，却互相作证，把“偶发冲突”一点点推离原位。

城市习惯把很多东西叫成意外。尤其在这样一个暴雨夜里，等着复位车进来、等着道路恢复、等着电话挂断的人太多了。每个人都希望事情能快点被命名，然后结束。

可有些事，一旦被草率命名，结束的就只是追问它的人。

陆深最后又看了一次那道金属新伤，对旁边人说：“工具箱单独封存。井盖今晚不复位。”

他的目光落在那格空位上，心里已经很清楚——

缺失的不只是某件工具。

缺失的是这场雨原本想替谁抹掉的那一步。

