# 1.1.1 不是斗殴，是摆放现场

如果你问我，是什么时候决定彩虹巷那具尸体不是街头斗殴，我会回答：看见那把刀的第一秒。

那天夜里雨很大。不是普通的下雨，是老城区所有楼顶一起往下倒脏水那种大。彩虹巷尽头半掀着一口井，井盖被立在墙边，像给后来的调查者摆了个路标。井口外侧躺着一具男尸，右手边是一把荧光美工刀。

很多人第一眼看到的是尸体。

我先看到的是那把刀放得太顺。

辖区民警比我先到，雨衣都湿透了，见我进来就把口头情况压缩给我：男性，三十多岁，身上有基站维护证件；巷口摊主听见一阵动静，以为有人打架；分局在催初步口径，现场如果再拖，细痕就要被雨洗掉。

他的意思我明白：先按街头斗殴写，至少流程走得快。

问题就在这里。案子一旦先被写成某种样子，后面的证据常常要花更大的代价，才能把这句早到的结论赶出去。很多误判不是从证据开始的，是从图省事开始的。

我蹲到尸体右边，先看刀和手的关系，再看尸体与井口的关系。刀不算离死者太远，但它的方向不对。刀尖朝外，刀柄朝内，几乎像有人替它选过一个“第一眼就像凶器”的位置。粗水泥地、旧油渍、积水、砂粒，全都说明一件事：如果真是暴雨夜冲突后脱手，它至少该有翻滚、擦蹭、转折，而不是现在这种过分工整的安静。

这是第一个理由。

第二个理由，来自刀柄。

荧光色不是刀自带的，是后来涂上去的亮漆。雨水越冲，它看上去越冷。我用侧光扫了一遍，握持区有细密拖痕，圈着手掌会压住的位置，边缘反而比刀身还干净。那不是正常使用留下的纹理，是擦拭留下的痕。

一把被放对位置、还被重点擦过的刀，如果只是街头斗殴现场的“意外遗留”，那这场意外也太懂程序了。

我让辅警先记：二号物刀柄疑似人工擦拭，重点送检指纹残留与表面纤维。

民警还想跟我争一句：“就凭一把刀摆得顺，不足以推翻一起暴雨夜冲突案吧？”

我把他的记录板拿过来，在“初步判断”后面写了四个字：物证存疑。

这不是跟他抬杠，是给后面的证据腾位置。

尸体身份很快确认：何川，维保工程师。身上的工牌、门禁卡都在，编号 A区外维-07。旁边的工具箱应该是他的。箱子打开着，里面大多数工具在位，只有中间一格空着。那格尺寸不大，适合放一件短而结实的撬类工具。海绵边缘有新切口。很明显，那里原本有东西，现在被拿走了。

到这里，你也许会说，工具少一件，不一定就是案子。

所以我要说第三个理由。

井盖内侧有一道不属于常规维保的工具痕。

老城区这种铸铁井盖，平时怎么开，我知道。外缘两道正常撬痕方向一致，说明标准起井钩用过。但靠锁耳那一侧，还有一小段新鲜金属咬痕，更窄，受力更陡，边缘微翻。那不是井钩，也不是正常扳手辅助。更像某种更尖、更硬的东西，从内侧狠狠干进去过一次。

我让人递来比例尺，贴着拍完，脑子里已经有了对应物：短柄撬片。

我回头再看工具箱，清单编号 TL-07B 上，短柄撬片那一栏对应的格子，正好空着。

刀不对，井盖不对，工具少得也刚刚好。

这三件事如果还要解释成巧合，那就不是调查，是安慰自己了。

法医还没到。我先看何川的手。虎口和指节有明显老茧，符合长期握工具的工作习惯；指尖却过分干净，不像刚经历过一场真正的剧烈扭打。左前臂靠近袖口处有一道擦伤，带着黑灰，方向平拖，不像刀割，更像和井沿或金属内壁摩擦形成的损伤。民警说，目击者只听见一声闷响，像井盖砸地，没听见清楚的呼救。报警时间 22:03，到场时间 22:08。

如果何川真在巷子里和人狠狠干起来，现场不该这么收束。尸体、井口、井盖、那把刀，全都像围着一个结论摆出来的。

这时候林岚到了。

她总是比多余的话晚一步，比正确的程序快一步。她进线后的第一句话不是问“什么情况”，而是问：“谁动过？”

我说没人乱碰，刀柄被擦过，井盖内侧有异常痕，工具箱缺项。

她点头，转身就对周围人说：“先封，再说判断。谁动了哪儿，回头都得落纸。”

很多时候，调查不是靠某个聪明判断赢的，是靠不让现场先输。林岚在这件事上和我一直是同一套脾气。

她很快在死者上衣内袋里翻出一张热敏纸回执。

临时派修单。
工单号 WX-LS-2147。
地点：西区彩虹巷禁入段边井。
签到时刻：21:32。
处理级别：加急。
最关键的是，“前置报障”一栏，空白。

这里逻辑才真正开始咬合。

如果前置报障为空，这张加急派修单是怎么跑出来的？
如果今晚没有登记开盖，何川为什么会来？
如果他是被正当派遣来的，工具箱里那件最能对应井盖新伤的工具，为什么会消失？

就在我检查工具箱空槽边框的时候，指尖隔着手套碰到金属，脑子里短了一下。那不是我能写进任何报告里的东西。我只看见一个发力的角度，一道往里别开的动作，随后一个时间很清楚地撞出来：21:47。

我把手收回来，装作无事发生。

因为真正有用的，不是我说我“感觉到了什么”，而是我能不能把别的证据找出来，去验证21:47和纸面上的21:32到底谁在说谎。

林岚把派修单举给我看时，我脑子里就是这个念头。
21:32。
21:47。
两者差了十五分钟。

这十五分钟太短，短到很多人会忽略；又太长，长到足够让一个人提前开井、布置位置、拿走工具、再把刀放好。

物业的回话随后补上了这条链的最后一块信息：值班室确认，今晚这口井没有登记开盖，系统里也没有对应报修。

至此，这就不再是“我个人觉得不像斗殴”。

而是派修单、开盖记录、工具缺失、刀柄擦拭、井盖内侧新痕，五个点同时向同一个方向收口：何川不是在这里随机跟人狠狠干起来的。他是被叫来的。来之前，这口井就已经有人动过。来之后，现场又被人重新摆过。

最后把这个结论钉死的，是抬尸那一下。

尸体一挪开，背下积水裂开，露出一小块更干的地面。那里有一道短短的拖拽弧线，方向朝井口。不是自然倒地留下的线，更像有人在把尸体放稳之前，往井口方向微微拖过一次位置。

到这里，我已经不需要再跟任何人解释“为什么不是街头斗殴”。

因为这个现场根本不是冲突后的狼藉。

它是被安排过的安静。

我对辖区民警说，不要先写随机行凶，先按疑似案件留证。他看了看那把刀，又看了看派修单，最后还是把原本准备写上去的说法擦掉了。

其实我理解他。暴雨夜里，谁都想快点结束。可有时候，越急着结束，越是在替真正动手的人清理路面。

林岚那边让人准备套尸袋，我则又把视线落回工具箱空位上。那一格像是故意空给我看的。有人开过井，有人拿走了工具，有人把何川放在井边，再把那把刀摆成一个廉价答案。

我最后又照了一次井盖内侧那道金属新伤，心里只剩一个判断：

这不是斗殴。

这是摆放现场。

而且，真正能开这口井的那件工具，现在还在某个人手里。

