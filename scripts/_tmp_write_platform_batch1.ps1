# 1.1.1 井口那把太顺的刀

陆深钻进彩虹巷的时候，雨已经把裤脚灌透了。

巷子尽头那口井先撞进他眼里。井盖半掀，沉重的铸铁盖被人立在墙边，像故意摆给后来的人看。井沿外，一具男尸斜倒在积水边，肩背压在粗糙水泥地。尸体右手外侧，躺着一把荧光美工刀。

不是刀本身吓人。

是它太整齐了。

老城区的雨越下越横，彩虹巷两边墙皮被冲得发黑，巷口便利店只剩半截招牌亮着。蓝红警灯从积水里晃上来，把那把刀照得像一条冷得发绿的鱼。

先到场的辖区民警正蹲着做登记，抬头看见他：“SCIU？”

陆深点了下头，没寒暄，先看刀，再看尸体，再看井口。

“男，三十多，身上带着基站维保证件。”民警说得飞快，“巷口烧烤摊有人听见动静，说像打架。分局催着先给个口径，初步准备按街头斗殴报。再拖下去，雨把细痕全冲没了。”

这话陆深太熟了。

有些案子不是死在真相里，是死在第一句省事的结论里。

一旦“街头斗殴”四个字先进表，后面就算查出监控、工单、门禁、物证全对不上，也得整本案卷往回拽。谁先写的，谁先挨问。

他蹲到尸体右侧，没碰人，先量眼前这点距离。井口离死者右肩不远，井盖立在左后方，刚好挡住一片雨。尸体像被放在一个最容易让人误会的位置上。那把荧光刀离死者右手也不算离谱，可就是差着一点劲道。

真在雨里打起来，刀脱手，不会这么顺。

不会刀尖朝外，刀柄偏向尸体。

更不会在这种粗水泥地上，一点翻滚擦蹭的狼狈都没有。

“身份呢？”陆深问。

辅警马上递来塑封证件袋：“何川，维保工程师。工牌、门禁卡都在。门禁卡编号 A区外维-07。井口边这个工具箱，应该也是他的。”

工具箱是金属的，卡扣已开，里头分格海绵大半还整齐。螺丝刀、测电笔、胶布、扎带、扳手都在，只有中间一格空着。那格不大不小，像原本搁着一件短而结实的撬类工具，现在被人拿走了。海绵边上还有新割出来的毛茬，像后来被刀子重新修过形。

陆深先看刀。

荧光色不是刀身自带的，是刀柄上涂了几道亮漆。雨一冲，那颜色不但没脏，反倒冷得扎眼。陆深用手电侧过去，看见握持那一圈有细细的拖痕，像被人擦过。不是随手抹一下，是知道该擦哪里的人擦的。

“动过吗？”他问。

“没。”民警指向定位牌，“照片拍了，物还没起。你看，不像打完架顺手掉的？”

“不像。”陆深说。

民警皱眉：“就因为摆得顺？”

“够了。”陆深看着那把刀，“搏斗里掉下来的东西，先乱，再散。它现在像被人教过姿势。”

民警张了张嘴，显然还想争。巷口那边已经有人在催，社区、物业、值班车、分局电话，全等着这边尽快通车。暴雨夜里，每个人都想快点结束。但案子不一样。案子最怕的，就是大家都想快。

陆深伸手，把对方手里的记录板接过来，在“初步判断”那栏写下四个字——物证存疑。

“这四个字我签。”他说，“后面谁要改，自己写理由。”

说完他把板子递回去，又点了两个人：“井口外沿、尸体背下地面、工具箱空槽，三处单拍、单取。巷口复位车先别进来。没有我签字，谁也别把这口井盖回去。”

地面全是旧油污和细砂。若是失控脱手，刀最少要带出一点划痕。可这把刀没有。反而干净得像特意留给第一眼的人去认。

法医还没到，陆深先看死者双手。右手虎口和指节有长期用力的老茧，符合维修工常年握工具的样子；但指尖太干净，不像刚狠狠干过一场。左前臂靠袖口的位置有一道擦伤，边缘蹭着黑灰，不像刀口，更像在井沿或者金属内壁上磨出来的。

“有人听见他喊没？”陆深问。

“没听清，就一声闷响。”民警答，“像井盖砸地。十点零三报警，十点零八我们到。”

陆深点了下头，视线终于移到井盖内侧。

老城区这种铸铁井盖，他见得多了。外缘两道常规撬痕方向一致，是标准起井钩留下的。可内侧靠锁耳那一小段，不一样。那道金属咬痕更窄，受力更陡，边缘还微微翻起，像有人拿了另一种更尖更硬的东西，从里面狠狠干过一次。

这不是常规维保会留下的口子。

“井盖到场时就这样？”陆深抬头。

“就半开。”民警答，“报案人路过看见腿，吓坏了。我们只确认过井下没人，没敢乱挪。”

“谁碰过？”

“我和第一到场的协警。”

陆深让人递来比例尺，贴在那道新伤旁边，低声说：“第三种工具痕。”

民警一愣：“第三种？”

“正常开这口井，两种工具够了。”陆深站起来，回头看向工具箱空槽，“这一道，不是那两种留下的。”

这下民警的脸色终于变了。他顺着陆深的目光望回去，像是也意识到哪儿不对：“你是说，还有别人开过井？”

陆深没正面回答，只把工具箱掀到最大。箱盖内侧有维保公司资产编码：WX-04-19。下方塑封工具清单编号 TL-07B，列着八样常规工具。陆深几乎没花时间，就停在其中一项上。

短柄撬片。

对应的格子，空着。

“死者身上有安全绳扣吗？”他问。

“没有。工具箱里也没有。”辅警答。

陆深没碰里头的东西，只用手电扫过槽位边缘。空位旁沾着一层极细的灰白粉末，像金属被狠狠干过之后留下来的屑。就在这一瞬间，他隔着手套，把手指在空槽边框上轻轻抵了一下。

一股极轻的麻意顺着指尖窜上来。

不是完整画面，只是短促的残压：一只手，拿着细长工具，从内侧发力。井盖先松，再抬。随后有一道时间戳一样的东西猛地撞进脑子里——21:47。

下一秒，感觉就断了。

陆深收回手，像什么都没发生。他知道这玩意儿没法写进记录里，也没法对任何人解释。他能做的，只有先把它压住，留在自己脑子里，等别的证据来咬它。

这时巷口又有人跑进来。

林岚套着雨衣和鞋套，进场比谁都利索。她先看尸体，再看井盖，再看陆深，问的第一句就是：“谁动过？”

“没人乱碰。”陆深把现场情况压成几句短话，“刀被擦过，井盖内侧异常痕，工具箱缺项。”

林岚嗯了一声，直接抬头对周围人说：“先封存，再谈判断。谁动过哪儿，都给我写清楚。”

她蹲下去，掀开死者工装外套，查了胸腹、颈侧、锁骨和后脑受力点，声音很稳：“正面没见明确致命刀伤。后脑疑似钝击。那把刀谁都别碰。”

“刀柄被擦过。”陆深说。

“那就更别碰。”林岚回他。

两个人对话不长，可周围人一下子都老实了。规矩一落纸，现场就没那么容易被一句“打架失手”糊过去。

巷外，物业保安在警戒带外头喊：“警官！值班室回电话了，说今晚这口井没登记开盖！”

陆深侧头：“谁回的？”

“夜班值守。说报修单里没有这项。”

“把通话时间记下来，别往外放风。”陆深说。

保安忙不迭点头。

也就在这时，林岚从死者上衣内袋里夹出一张折皱的热敏纸。雨水把纸边泡软了，但字还看得见。

临时派修单。
工单号：WX-LS-2147。
地点：西区彩虹巷禁入段边井。
签到时刻：21:32。
处理级别：加急。
而“前置报障”一栏，是空白的。

林岚把那张纸举高一点：“这个单独封存。还有，他袖口沾的不是地面泥，像井壁旧涂层。”

陆深盯着那张派修单，脑子里的两个时间一下子并到一块。

纸上 21:32。
手里闪过的 21:47。

十五分钟的错位，不大，却足够让人后背发紧。

何川不是路过。

他是被叫来的。

有人提前让他来这口井。有人知道禁入段边井今晚该被打开。甚至有人连现场要怎么摆，才最像一场便宜又仓促的街头冲突，都替他想好了。

“抬尸吧。”林岚说，“再等，地面痕就全没了。”

陆深绕到另一侧，又看了一遍工具箱底层。巡检单、防水袋、记号笔都在，只有那格空位一直刺眼。一个维修工带着整箱工具来现场，却偏偏少了最能对应井盖新伤的那件东西，这本身就已经不是偶然。

更糟的是，巷口那辆市政应急车还在等。

如果这边一松口，井盖会复位，积水会被抽走，巷子会恢复通行。再过一会儿，现场只剩一张表格、一句口径、一个被雨冲干净的夜晚。

民警凑过来，压低声音：“还不能定？”

“不能。”陆深说，“但可以先排除一件事。”

“什么？”

“不是街头斗殴。”

民警愣住。

陆深指了指刀，又指了指派修单，再指井盖内侧那道新痕：“刀被擦过；派修单前置报障空白，却走了加急；井盖内侧有第三种工具痕，工具箱里正好缺了对应工具。你今晚要是硬写随机冲突，明天这些东西会一起回来打你的脸。”

民警沉默几秒，到底把原来那句写了一半的话擦了。

这场雨里，最难的不是看见尸体。

是有人明明知道现场不对，还想先把它写对付过去。

尸体被抬起来的瞬间，积水裂开，下面露出一小块稍干的地面。那上头有一条短短的拖拽弧线，几乎被鞋印压坏，方向却很清楚——朝井口。

不是人打完架自己倒下的轨迹。

更像有人在放好尸体之前，往井口那边拖过一下，调了位置。

“看见没有？”林岚低声问。

“看见了。”陆深答。

到这一刻，“街头斗殴”四个字，已经彻底站不住了。

真正让他发冷的，也不是尸体，不是那把刀，而是三件事终于扣到了一起：井盖内侧那道不该出现的新伤，工具箱里空出来的那一格，还有那张前置报障空白却被加急派出来的临时工单。

有人先开了井。

有人拿走了工具。

有人把何川和这把刀，一起摆成了另一个故事。

陆深最后一次把手电压在井盖内侧那道工具痕上。水珠顺着翻卷的金属边往下滚，在光里亮了一下，像有人匆忙中漏掉的一根针。

他没再多看，只对身边人说：“工具箱单独封存。井盖今晚不复位。”

说完他盯着空槽，补了一句：

“少的那件东西，还在外面。”

