# 1.1.1 #暴雨巷口# 一把被摆好的刀

暴雨。
老巷。
半掀井盖。
一具男尸。
一把摆得太整齐的荧光美工刀。

陆深钻进彩虹巷时，先看到的不是尸体，是那把刀。

它太像答案了。

刀尖朝外，刀柄朝内，贴着死者右手外侧躺着，位置不近不远，刚好够第一眼的人说出一句最省事的话：街头斗殴，刀是死者自己的，现场结束。

而陆深最讨厌的，就是现场还没开口，答案先抢着发言。

辖区民警比他先到，雨衣已经全湿透了，说话也快：“男，三十多，身上带基站维保证件。巷口摊贩听见过动静，以为打架。分局催着先报口径，想先按街头斗殴走。再晚一会儿，细痕全没。”

这就是问题。

暴雨夜里，谁都想快。
快定性，快通车，快结束。
可案子最怕的，偏偏就是快。

陆深蹲下去。

先看刀和手。
再看尸体和井口。
再看井盖立的位置。

那把刀不算离死者太远，但远得不对。更别说方向。粗水泥地、旧油污、积水边，真正狠狠干起来之后脱手的刀，不会这么稳，不会这么干净，更不会像现在这样，像被人专门摆给后来的人看。

“身份。”陆深伸手。

辅警把塑封袋递过来：“何川。维保工程师。工牌、门禁卡都在，编号 A区外维-07。旁边这个工具箱也像是他的。”

工具箱打开着。
螺丝刀在。
扳手在。
测电笔在。
胶布和扎带也在。
偏偏中间空了一格。

那格的形状，很像短撬工具待的地方。

陆深把手电压回刀柄。

荧光漆在警灯下冷得发绿。握持区那一圈有细密拖痕，像被人狠狠干净过。不是旧磨损，是新擦痕。

刀不只是摆得不对。

它还被处理过。

民警还想再争一句：“也不能就凭这个，把整条巷子都按凶案封着吧？”

陆深接过记录板，在“初步判断”后面写下四个字：物证存疑。

“先让证据说话。”他说。

然后他把现场重点直接圈出来：井口外沿、尸体背下地面、工具箱空槽，全部单拍单取；巷口复位车先别进；井盖今晚不准先复位。

对付暴雨现场，能保一点是一点。

死者双手很快也给了反馈。虎口和指节有老茧，像常年握工具的人；指尖却太干净，不像刚狠狠干过一场。左前臂擦伤带黑灰，更像蹭过井沿或金属内壁。

“有人听见他喊吗？”陆深问。

“没听清。就一声闷响，像井盖砸地。十点零三报警，十点零八到场。”

陆深抬灯看井盖内侧。

外缘两道旧痕正常，标准起井钩留下的。
真正刺眼的是内侧锁耳附近那道新伤。
更窄。
更陡。
边缘微翻。
明显不是常规开盖工具的手法。

他拍完照，给了结论：“第三种工具痕。”

这句话一出来，现场就安静了半秒。

因为这时候，井盖里的新伤和工具箱里空着的那格，已经开始互相对上了。

陆深把工具箱翻到最大。箱盖内侧贴着资产编码 WX-04-19，塑封清单编号 TL-07B。八样常规工具里，短柄撬片对应的格子正空着。

而空位边缘，粘着一点极细的灰白粉末，像金属狠狠干过之后留下的屑。

陆深隔着手套，在边框上轻轻碰了一下。

一瞬间，他脑子里闪过一段极短的碎片：有人从井盖内侧发力，工具扣住边缘，往上一挑。随后一个数字狠狠干进来——21:47。

下一秒，全没了。

他收回手，像什么都没发生。

这个时间还不能上桌。
但它已经卡在他脑子里了。
等着去碰别的证据。

林岚就是这时候进来的。

她一脚踩进积水，第一句话不是“什么情况”，而是“谁动过”。

陆深回她：“刀柄被擦过。井盖内侧第三种工具痕。工具箱缺项。没人乱碰。”

林岚点头，直接对周围人说：“先封，再说判断。谁碰过哪儿，都落纸。”

这句话比任何争论都管用。

她检查了死者外表伤，很快说：“正面没见明确致命刀伤。后脑疑似钝击。刀先别碰。”

这就更有意思了。

最抓眼的那把刀，可能根本不是杀人的那一把。

巷外物业保安隔着警戒带喊：“值班室回电话了！今晚这口井没登记开盖！”

与此同时，林岚从死者上衣口袋里夹出一张热敏纸。

临时派修单。
工单号 WX-LS-2147。
地点：西区彩虹巷禁入段边井。
签到时刻：21:32。
处理级别：加急。
最关键的是，“前置报障”空白。

这下故事彻底变味。

何川不是路过，他是被派来的。
可如果今晚没有正式开盖记录，他为什么会来？
如果是正常派修，为什么前置报障空着？
如果现场真是斗殴，为什么井盖内侧有第三种工具痕，工具箱里又正好少了短柄撬片？
还有，为什么陆深脑子里冒出来的时间是21:47，而纸上写的是21:32？

十五分钟。

够做很多事。
够提前开井。
够拿走工具。
够把刀摆好。
也够把一个人放到最合适被看见的位置上。

“先抬尸。”林岚说。

尸体一抬，背下积水裂开，露出一小块更干的地面。上面有一道很短的拖拽弧线，方向正朝井口。

不是自然倒地。
是被调过位置。

到这里，“街头斗殴”四个字已经彻底站不住了。

刀被擦过。
井盖内侧第三种工具痕。
工具箱缺了短柄撬片。
前置报障空白，却走了加急派修。
21:32 对不上 21:47。
尸体背下还有拖拽调整痕。

单条拿出来，还能争。
一起摆在雨里，就只剩一个意思：

这不是随机斗殴。
这是摆放现场。

陆深看着辖区民警：“别先写随机行凶。按疑似案件留证。”

对方沉默了几秒，还是把原来那句话擦掉了。

这场雨里真正可怕的，不只是尸体。

是有人比雨下得更快，想先把口径盖下去。

陆深最后又看了一眼工具箱那格空位，只说了两句：

“工具箱单独封存。”
“井盖今晚不复位。”

停了停，他又补了一句：

“开这口井的东西，不在现场了。”

