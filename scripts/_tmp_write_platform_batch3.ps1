# 1.1.1 开场就不对：这不是随机斗殴

如果把这一幕拆成三个镜头，第一镜是半掀的井盖，第二镜是尸体右手边的荧光刀，第三镜才轮到雨里走进来的陆深。

问题也正出在第二镜。

彩虹巷的暴雨夜，最先抓住人眼睛的，不是尸体，而是那把刀。荧光漆在警灯下发冷，刀尖朝外，刀柄朝内，躺得过分工整。它不像从谁手里飞出来，更像被谁放在这儿，专门等第一眼的人给它配一句台词——凶器。

陆深一钻过警戒带，鞋底就踩裂一层油膜水。蓝红光在积水里抖，巷口便利店半块招牌明灭不定，墙边那块铸铁井盖像临时立起来的黑幕。尸体就倒在井口外，肩背压着水泥地，像被整条巷子收进了最窄的一格里。

先到场的辖区民警见他进来，直接把情况往前推：“男，三十多，身上有维保证。巷口摊贩听见动静，说像打架。分局在催口径，想先按街头斗殴报。雨再下，现场就没法看了。”

这一段如果配弹幕，估计第一反应会很统一：先定性，后补证。

陆深偏偏最烦这种路子。

现场还没说话，口径先抢话筒。案子一旦先被写成“街头斗殴”，后面就算监控、工单、门禁、物证一条条顶回来，也是在跟第一句省事话打架。

他没急着驳，只是先蹲下。

镜头往下压，先看尸体和井口的关系，再看刀和手的关系。井口离死者右肩不远，井盖立在左后方，挡住一片雨。刀离死者右手也不算远，但方向太“像回事”了。真正狠狠干起来之后脱手的刀，应该乱、偏、滑、翻，不会像现在这样，干净得像给人留的道具。

“身份。”陆深抬手。

辅警把塑封袋递过来：“何川，维保工程师。工牌、门禁卡都在，A区外维-07。工具箱应该也是他的。”

镜头再切到工具箱。

金属箱摊在井口另一边，卡扣开着，海绵分格大半还整齐。螺丝刀、扳手、测电笔、胶布都在，偏偏中间空了一格。那格的尺寸很适合放一件短撬工具，海绵边缘有新切口，像后来又被刀修过。

陆深先不碰箱子，反手把手电压回刀柄。

特写来了。

荧光漆湿冷发亮，握持区有一圈很细的拖痕，像被人狠狠干净过。不是旧磨损，是新擦痕。也就是说，这把刀不光位置不对，它还被人处理过。

“有人动过没有？”陆深问。

“没。照片拍完了，物还没起。”民警说完，还是忍不住问，“可也不能因为它摆得顺一点，就按凶案封全巷吧？”

陆深接过记录板，在“初步判断”后面写下四个字：物证存疑。

这四个字一写，节奏就变了。

他随手点了三处重点：井口外沿、尸体背下地面、工具箱空槽，全部单拍单取；巷口复位车先别进；今夜这口井不能急着合上。看似是程序，其实是把现场从“快点结束”的情绪里硬拽回来。

接着是人身细节。

死者右手虎口和指节有老茧，明显长期握工具；但指尖太干净，不像刚狠狠干过一场。左前臂靠袖口的擦伤带着黑灰，更像井沿或金属内壁留下的摩擦，而不是刀伤。

“有人听见他喊吗？”陆深问。

“没听清，只听见一声闷响，像井盖砸地。十点零三报警，十点零八到场。”民警答。

然后，镜头终于推到真正关键的那个特写：井盖内侧。

外缘旧痕正常，标准起井钩的痕迹，老城区做维保的人都认得。可靠锁耳那段新伤明显不对。更窄，更深，受力更陡，边缘微翻，像被另一种更尖更硬的工具狠狠干进去过一次。

陆深让人递来比例尺，贴着拍完，说了一句：“第三种工具痕。”

这一句一出来，现场短暂停了半秒。

因为这时候，镜头得回切工具箱——中间那格空位，清单编号 TL-07B，对应的正是短柄撬片。井盖里多出第三种工具痕，工具箱里少的正好是短柄撬片，这就不是“巧”，而是“对”。

辅警又补了一句：死者身上没有安全绳扣，工具箱里也没有。

陆深低头扫过空位边缘，那里粘着一点极细的灰白粉末，像金属磨削后留下的屑。他隔着手套，在边框上轻轻碰了一下。

下一秒，他脑子里闪出一段不该现在出现的画面：有人从井盖内侧发力，工具咬住边缘，往上一挑。反光一闪。随后一个数字狠狠干进来——21:47。

感觉断得很快。

他把手收回来，什么都没说。

这个时间还不能上桌，但它已经像一条暗轨，在脑子里和别的线索并排跑了起来。

这时林岚进场。

如果要给这一镜配人物标签，那就是：程序护栏。她套着鞋套踩进水里，先扫尸体、井盖、刀，再问一句“谁动过”。陆深把情况压成最短三句：刀柄被擦过，井盖内侧有第三种工具痕，工具箱缺项。

林岚点头，直接对周围人说：“先封，再说判断。谁碰过哪儿，都落纸。”

这一下，现场的气压都降了。

她检查死者表面伤，很快给出一句更要命的话：“正面没见明确致命刀伤。后脑疑似钝击。刀先别碰。”

也就是说，那把最抓眼的刀，很可能就是摆给人看的。

巷外物业保安隔着警戒带喊：值班室回电话了，今晚这口井没排开盖，系统里没登记。

再切回林岚手里。她从死者上衣内袋里夹出一张热敏纸回执。

临时派修单。
工单号 WX-LS-2147。
地点：西区彩虹巷禁入段边井。
签到时刻：21:32。
处理级别：加急。
而“前置报障”四个字后面，是空白。

到这里，讨论点就全起来了。

何川不是路过，而是被派来的。
可为什么前置报障是空的？
为什么系统里没有正式开盖记录？
为什么井盖内侧有第三种工具痕？
为什么工具箱里偏偏少了短柄撬片？
为什么陆深刚才碰到边框时，脑子里闪出的是21:47，而纸上写着21:32？

十五分钟的错位，不算大，却够把现场整个重构一遍。

够有人提前开井。
够有人拿走工具。
够有人把刀放好。
也够有人把尸体摆进“看着像随机冲突”的位置。

“先抬尸。”林岚说。

这一镜是本章真正的回报。

尸体一抬，背下积水裂开，下面露出一小块稍干的地面。那上头有一道短短的拖拽弧线，方向朝井口。不是自然倒下能留出来的痕，更像在最后停稳之前，被人朝井口方向轻轻调过一次。

这一下，所谓“随机斗殴”就彻底没戏了。

陆深这才转头对辖区民警说：“先别写随机行凶。不是街头斗殴。”

民警问他凭什么。

陆深一条条摆给他听：刀的位置不对，刀柄擦过；井盖内侧是第三种工具痕；工具箱少了短柄撬片；派修单前置报障空白却走加急；值班室说今晚没登记开井；尸体背下还有拖拽弧线。

单条看像悬念，合在一起就是结论。

有人提前动过井。
有人带走了工具。
有人想把整件事拍成另一部片子。

巷口的复位车还在等，雨也没停，整座城都在催这条巷子快点恢复正常。但陆深知道，今晚最不能快的，恰恰是结论。

他最后照了一次井盖内侧那道金属新伤，对身边人说：“工具箱单独封存。井盖今晚不复位。”

停了半秒，他盯着工具箱那格空位，又补了一句：

“能开这口井的东西，现在不在这儿。”

