# 1.1.1 雨夜巷子里，那把刀摆得不对劲

雨先钻进后颈的时候，陆深还没看见尸体。

他先看见一抹发绿的光。

彩虹巷太窄了，窄得雨水都像贴着墙往下滑。巷口那半块便利店招牌一闪一闪，警灯落进积水里，晃出来的蓝红光被鞋底踩碎。就在最深那段水泥地上，半掀的井盖立在墙边，像一面又黑又湿的盾。井沿外，躺着一个男人。男人右手边，是一把荧光美工刀。

那刀放得太安静。

安静得不像刚从谁手里挣出来，更像刚刚被人放下。

陆深弯腰钻进警戒带，裤脚和鞋帮一瞬间全凉透了。老城区的雨有股很旧的味道，铁锈、污水、墙皮和发霉电线混在一起，顺着鼻腔往里钻。他没先去看死者脸，也没急着听辖区民警汇报，先盯住了那把刀。

刀尖朝外，刀柄朝内，位置像被人挑过。

“SCIU？”辖区民警喊他。

陆深嗯了一声。

对方说得很快，像生怕再慢一点，这条巷子就要被雨洗空：“男，三十多，身上有基站维保证。巷口烧烤摊有人听见动静，说像是打架。分局在催口径，想先按街头斗殴走。再拖下去，现场细痕全没。”

“街头斗殴”这四个字一出来，陆深后槽牙就轻轻咬了一下。

他见过太多这种时候。不是没人想查，是所有人都被雨、时间、电话、通车、程序往前推，推到最后，最方便的解释先落纸，真正麻烦的那一层被压在后面。可案子不是这么办的。案子一旦先被一句省事的话盖住，后面再翻，翻出来的就不止是证据，还有每个人怕麻烦的那点心思。

他蹲下去，看尸体和井口的距离。

井口离死者右肩不远，井盖立在左后方，挡掉一小块雨。尸体像被卡在这口井和这条巷子的夹缝里。右手边那把刀不远不近，偏偏差一点真实。要真是狠狠干起来之后脱手，它不会这么平，不会这么稳，不会在粗水泥地和积水边上一点狼狈都没有。

“身份。”陆深伸手。

辅警把塑封袋递给他：“何川，维保工程师。工牌、门禁卡都在，A区外维-07。井口边那个工具箱应该是他的。”

工具箱摊开着。螺丝刀、扳手、测电笔、胶布都在，只有中间一格空着。那格不算大，形状很适合卡一件短撬工具。海绵边缘有新切口，像被谁急急修过。

陆深又把手电压回刀柄。

荧光漆被雨洗过以后更冷了，像湿墙里渗出来的光。握持区有一圈很细的拖痕，像被布或者手套狠狠干净过。不是正常使用留下的旧痕，是有人临走之前，特意把这里处理过。

“有人碰过没？”他问。

“没。照片拍了，物还没起。”民警说完，又忍不住问，“可也不能就凭它摆得顺一点，把这里按凶案封一夜吧？”

陆深把记录板接过去，在“初步判断”后面写了四个字：物证存疑。

他写得很稳。

因为这会儿最需要稳。

随后他点人固定三处：井口外沿、尸体背下地面、工具箱空槽。又把巷口那辆等着复位的应急车先压住。那辆车只要一动，很多东西就真没了。

雨打在井盖上，声音像有人隔着铁皮连续敲指节。陆深蹲低去看死者的手。右手虎口和指节有老茧，像长期握工具的人；指尖却太干净，不像刚狠狠干过一场。左前臂靠袖口那片擦伤沾着黑灰，感觉更像蹭过井沿、管壁或金属内侧。

“有人听见他喊吗？”他问。

“没听清，就一声闷响，像井盖砸地。”民警答，“十点零三报警，十点零八到场。”

陆深这才把灯挪进井盖内侧。

外缘两道旧痕正常，标准起井钩留下的。可锁耳附近那道新伤很扎眼：更窄，更陡，边缘微翻，像被另一种更尖更硬的工具狠狠干进去过一次。那不是平时维保会留下的手法。

他让人递比例尺，拍完以后，声音压得很低：“第三种工具痕。”

民警没立刻接话，表情却变了。

因为这时候，井盖内侧的新伤，和工具箱里空着的那格，已经开始互相照镜子。

陆深走到工具箱边，掀开箱盖内侧的塑封清单。资产编码 WX-04-19，工具清单编号 TL-07B。八样常规工具里，短柄撬片那一项，对应格位正空着。

空位边缘还粘着一点极细的灰白粉末，像金属磨削后留下来的屑。

陆深隔着手套，在空槽边框上轻轻碰了一下。

掌心一下凉得发麻。

不是完整画面，只是一点贴脸的残压：有人从井盖内侧发力，工具扣进去，往上一挑；水从井边甩开；有反光；再然后，一个数字狠狠干进脑子里——21:47。

下一秒，全断了。

陆深把手收回来，指尖却像还沾着那一下的冷。他没说出来。说不出来的东西，暂时只能压住。能留在案卷里的，还得是看得见、拍得到、能让别人重复验证的痕迹。

林岚就是这时候进场的。

她冒雨走进来，鞋套踩进水里，先看尸体，再看井盖，再看那把刀：“谁动过？”

“没人乱碰。”陆深说，“刀柄被擦过，井盖内侧第三种工具痕，工具箱缺项。”

林岚点头，转身就把规矩压下去：“先封，再说判断。谁碰过哪儿，都落纸。”

她蹲下去检查死者表面伤，过了几秒才开口：“正面没见明确致命刀伤。后脑疑似钝击。刀先别碰。”

“刀柄擦过。”陆深补了一句。

“那就更别碰。”林岚看了他一眼。

那一眼很短，却像把现场又收紧了一层。

巷外物业保安隔着警戒带喊：“值班室回电话了！今晚这口井没登记开盖！”

陆深抬头：“谁回的？”

“夜班值守。系统里没这项。”

林岚这时正好从死者上衣内袋里夹出一张热敏纸。纸边被雨泡软，摸上去又冷又黏。

临时派修单。
工单号 WX-LS-2147。
地点：西区彩虹巷禁入段边井。
签到时刻：21:32。
处理级别：加急。
最下边，“前置报障”空白。

雨好像在这一刻更凉了。

21:32。
21:47。
两串数字一前一后卡进陆深脑子里，差了十五分钟。

十五分钟够做什么？

够提前开井。
够拿走一件工具。
够把一个人摆到最该被看见的位置上。
也够把一把刀放得像“答案”。

林岚把派修单举起来：“这个单独封存。还有，他袖口这层灰不是路面泥，像井壁旧涂层。”

这下，何川不再像一个倒霉路过的人。

他是被叫来的。

可如果是正常派修，为什么前置报障空着？如果今晚没有登记开盖，他为什么会来？如果他真是来维修，为什么工具箱里偏偏少了最能对应井盖内侧新伤的那件东西？

“先抬尸。”林岚说。

尸体一抬，背下积水裂开，下面露出一小块更干的地面。那里有一道短短的拖拽弧线，方向朝井口。很浅，却足够说明问题：这不是人自然倒下后留下的形状，更像有人在放稳尸体之前，往井口那边轻轻拖过一下。

那一瞬间，陆深心里那句话终于彻底落地。

不是街头斗殴。

是摆放现场。

刀被擦过。
井盖内侧有第三种工具痕。
工具箱少了短柄撬片。
派修单前置报障空白，却写着21:32加急签到。
他的掌心却看见了21:47。
尸体背下还有被调整过的拖拽痕。

每一条单拎出来都能解释，合在一起就只剩一个方向——有人提前动过井，有人带走了工具，有人想让这一夜看起来更像别的东西。

陆深最后又看了一眼工具箱那格空位，冷声交代：“工具箱单独封存。井盖今晚不复位。”

停了停，他补了一句：

“少的那件工具，现在还在外面。”

